CREATE TABLE GIAOVIEN (
	MAGV char (5),
	HOTEN nvarchar (40),
	LUONG float,
	PHAI char (3),
	NGSINH date,
	DIACHI nvarchar (50),
	GVQLCM char (5),
	MABM char (5),
	PRIMARY KEY (MAGV)
)

CREATE TABLE GV_DT (
	MAGV char (5),
	DIENTHOAI char (11)
	PRIMARY KEY (MAGV, DIENTHOAI),
	FOREIGN KEY (MAGV) REFERENCES GIAOVIEN(MAGV)
)

CREATE TABLE NGUOITHAN (
	MAGV char (5),
	TEN nvarchar (40),
	NGSINH date,
	PHAI nvarchar (10),
	PRIMARY KEY (MAGV, TEN),
	FOREIGN KEY (MAGV) REFERENCES GIAOVIEN(MAGV)
)

CREATE TABLE KHOA (
	MAKHOA char (5),
	TENKHOA nvarchar (40),
	NAMTL int,
	PHONG char (5),
	DIENTHOAI char (11),
	TRUONGKHOA char (5),
	NGAYNHANCHUC date,
	PRIMARY KEY (MAKHOA),
	FOREIGN KEY (TRUONGKHOA) REFERENCES GIAOVIEN(MAGV),
)

CREATE TABLE BOMON (
	MABM char (5),
	TENBM nvarchar (40),
	PHONG char (5),
	DIENTHOAI char (11),
	TRUONGBM char (5),
	MAKHOA char (5),
	NGAYNHANCHUC date,
	PRIMARY KEY (MABM),
	FOREIGN KEY (MAKHOA) REFERENCES KHOA(MAKHOA),
	FOREIGN KEY (TRUONGBM) REFERENCES GIAOVIEN(MAGV)
)

ALTER TABLE GIAOVIEN ADD CONSTRAINT FK_MABM FOREIGN KEY (MABM) REFERENCES BOMON(MABM)
ALTER TABLE GIAOVIEN ADD CONSTRAINT FK_GVQLCM FOREIGN KEY (GVQLCM) REFERENCES GIAOVIEN(MAGV)

CREATE TABLE THAMGIADT (
	MAGV char (5),
	MADT char (5),
	STT char (5),
	PHUCAP float,
	KETQUA nvarchar (20),
	PRIMARY KEY (MAGV, MADT, STT),
	FOREIGN KEY (MAGV) REFERENCES GIAOVIEN(MAGV)
)

CREATE TABLE CONGVIEC (
	MADT char (5),
	SOTT char (5),
	TENCV nvarchar (50),
	NGAYBD date,
	NGAYKT date,
	PRIMARY KEY (MADT, SOTT)
)

ALTER TABLE THAMGIADT ADD CONSTRAINT FK_MADT_STT FOREIGN KEY (MADT, STT) REFERENCES CONGVIEC(MADT, SOTT)

CREATE TABLE DETAI (
	MADT char (5),
	TENDT nvarchar (50),
	CAPQL nvarchar (20),
	KINHPHI float,
	NGAYBD date,
	NGAYKT date,
	MACD char (5),
	GVCNDT char (5),
	PRIMARY KEY (MADT),
	FOREIGN KEY (GVCNDT) REFERENCES GIAOVIEN(MAGV)
)

ALTER TABLE CONGVIEC ADD CONSTRAINT FK_MADT FOREIGN KEY (MADT) REFERENCES DETAI(MADT)

CREATE TABLE CHUDE (
	MACD char (5),
	TENCD nvarchar (40),
	PRIMARY KEY (MACD)
)

ALTER TABLE DETAI ADD CONSTRAINT FK_MACD FOREIGN KEY (MACD) REFERENCES CHUDE(MACD)

